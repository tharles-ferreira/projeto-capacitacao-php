                <p>
                    O orçamento público é um dos principais instrumentos de gestão do Estado. Por meio dele, o governo planeja quanto espera arrecadar e define como os recursos públicos serão aplicados para atender às necessidades da sociedade. É no orçamento que são previstas as despesas destinadas à manutenção dos serviços públicos e à execução de políticas públicas nas áreas de saúde, educação, segurança, infraestrutura, assistência social, entre outras.
                </p>
                <br>
                <p>
                    Mais do que um documento financeiro, o orçamento representa uma ferramenta de planejamento que orienta a atuação da Administração Pública e permite que as ações governamentais sejam executadas de forma organizada, transparente e responsável. Sua elaboração considera as prioridades estabelecidas pelo governo, os recursos disponíveis e as normas legais que disciplinam a gestão das finanças públicas.
                </p>
                <br>
                <p>
                    O Manual Técnico de Orçamento (MTO) define o orçamento como um instrumento de planejamento que representa o fluxo previsto de ingressos e aplicações de recursos em determinado período, possibilitando a execução das políticas públicas. As receitas orçamentárias constituem as disponibilidades financeiras que ingressam no patrimônio público, enquanto as despesas representam a aplicação desses recursos para atender às finalidades do Estado.
                </p>
                <br>
                <p>
                    No Brasil, o processo orçamentário está fundamentado na Constituição Federal e em diversas normas, como a Lei nº 4.320, de 1964, e a Lei Complementar nº 101, de 2000 (Lei de Responsabilidade Fiscal). Essas normas estabelecem as regras para elaboração, aprovação, execução e controle do orçamento, promovendo maior eficiência na gestão dos recursos públicos e fortalecendo a transparência e o controle social.
                </p>
                <br>
                <p>
                    Ao longo deste curso, serão apresentados os principais conceitos relacionados ao orçamento público, sua fundamentação legal, o processo de elaboração das leis orçamentárias e os mecanismos que permitem à sociedade acompanhar e fiscalizar a aplicação dos recursos públicos. O objetivo é proporcionar uma visão clara e acessível sobre um tema essencial para a administração pública e para o exercício da cidadania.
                </p>
